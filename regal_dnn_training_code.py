#get params from filename
def parse_distance_from_filename(filepath):
    basename = os.path.basename(filepath)
    match = re.search(r"_d([\d.]+)_", basename)
    if match:
        return float(match.group(1))
    match = re.search(r"([\d.]+)Mpc", basename, re.IGNORECASE)
    if match:
        return float(match.group(1))
    raise ValueError(
        f"Could not find distance from filename: {basename}\n"
        "Expected '_d<distance>_' or '<distance>Mpc' in the filename."
    )


def parse_ellipticity_from_filename(filepath):
    basename = os.path.basename(filepath)
    match = re.search(r"_e([\d.]+)", basename)
    return float(match.group(1)) if match else None


def galaxy_group(filepath):
    """Strip augmentation/rotation/residual/cropped suffixes -> base galaxy name."""
    basename = os.path.basename(filepath)
    basename = re.sub(r"\.(fits|npz)$", "", basename)
    return re.sub(r"_aug\d+_rot\d+(_residual)?(_cropped)?$", "", basename)

#to have stability; replace batch norm with group norm
def replace_bn_with_gn(module, num_groups=8):
    for name, child in module.named_children():
        if isinstance(child, nn.BatchNorm2d):
            num_channels = child.num_features
            g = num_groups
            while num_channels % g != 0:
                g -= 1
            setattr(module, name, nn.GroupNorm(g, num_channels))
        else:
            replace_bn_with_gn(child, num_groups)

def distance_bins(log_distances, n_bins=10):
    bins = np.linspace(log_distances.min(), log_distances.max(), n_bins + 1)
    return np.digitize(log_distances, bins, right=True)

