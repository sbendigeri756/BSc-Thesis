#grad-cam heat maps
class GradCAM:
    def __init__(self, model: nn.Module, target_layer: nn.Module):
        self.model = model
        self.activations = None
        self.gradients   = None
        self._fwd = target_layer.register_forward_hook(self._save_act)
        self._bwd = target_layer.register_full_backward_hook(self._save_grad)

    def _save_act(self, m, i, o):    self.activations = o.detach()
    def _save_grad(self, m, gi, go): self.gradients   = go[0].detach()

    def __call__(self, inp: torch.Tensor) -> np.ndarray:
        self.model.eval()
        inp = inp.requires_grad_(True)
        out = self.model(inp)
        self.model.zero_grad()
        out.sum().backward()
        weights = self.gradients.mean(dim=(2, 3), keepdim=True)
        cam = (weights * self.activations).sum(dim=1).squeeze()
        cam = torch.clamp(cam, min=0)
        vmin, vmax = cam.min(), cam.max()
        if vmax > vmin:
            cam = (cam - vmin) / (vmax - vmin)
        return cam.cpu().numpy()

    def remove(self):
        self._fwd.remove()
        self._bwd.remove()


