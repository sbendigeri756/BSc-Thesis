def select_gradcam_samples(dataset, bin_width_mpc=25):
    dists = 10 ** dataset.log_distances
    d_min = int(dists.min())
    d_max = int(dists.max()) + 1
    bins = range(d_min, d_max, bin_width_mpc)
    selected = []
    rng = np.random.default_rng(42)

    for b_lo in bins:
        b_hi = b_lo + bin_width_mpc
        in_bin = np.where((dists >= b_lo) & (dists < b_hi))[0]
        if len(in_bin) == 0:
            continue
        for compare in [
            lambda e: e is not None and e >= 0.4,
            lambda e: e is not None and e <= 0.1,
        ]:
            candidates = [
                i for i in in_bin
                if compare(parse_ellipticity_from_filename(dataset.paths[i]))
            ]
            if candidates:
                selected.append(int(rng.choice(candidates)))

    seen, unique = set(), []
    for i in selected:
        if i not in seen:
            seen.add(i)
            unique.append(i)
    print(f"Grad-CAM: selected {len(unique)} images across distance range")
    return unique

