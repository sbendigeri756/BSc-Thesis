def compute_metrics(preds, labels):
    p, l = preds.numpy(), labels.numpy()
    mae  = mean_absolute_error(l, p)
    rmse  = float(np.sqrt(np.mean((p - l) ** 2)))
    r2 = r2_score(l, p)
    med_err_mpc  = float(np.median(np.abs(10**p - 10**l)))
    mean_err_mpc = float(np.mean(np.abs(10**p - 10**l)))
    frac_err = np.abs(10**p - 10**l) / (10**l)
    within_5pct  = float((frac_err <= 0.05).mean())
    within_10pct = float((frac_err <= 0.10).mean())
    res = {
        "MAE_log": mae, "RMSE_log": rmse, "R2": r2,
        "MedianErr_Mpc": med_err_mpc, "MeanErr_Mpc": mean_err_mpc,
        "within_5pct": within_5pct, "within_10pct": within_10pct,
    }
    dist_true = 10 ** l
    for mask, band in [
        (dist_true < 60.0,   "<60 Mpc"),
        (dist_true >= 60.0,  ">=60 Mpc"),
        (dist_true < 100.0,  "<100 Mpc"),
        (dist_true >= 100.0, ">=100 Mpc"),
    ]:
        if mask.sum() < 2:
            continue
        p_s, l_s = p[mask], l[mask]
        res[f"MAE_log_{band}"]  = mean_absolute_error(l_s, p_s)
        res[f"RMSE_log_{band}"]  = float(np.sqrt(np.mean((p_s - l_s) ** 2)))
        res[f"R2_{band}"]  = r2_score(l_s, p_s)
        res[f"MedianErr_Mpc_{band}"] = float(np.median(np.abs(10**p_s - 10**l_s)))
        res[f"MeanErr_Mpc_{band}"]   = float(np.mean(np.abs(10**p_s - 10**l_s)))
        frac_s = np.abs(10**p_s - 10**l_s) / (10**l_s)
        res[f"within_5pct_{band}"] = float((frac_s <= 0.05).mean())
        res[f"within_10pct_{band}"]  = float((frac_s <= 0.10).mean())
        res[f"N_{band}"] = int(mask.sum())
    return res


