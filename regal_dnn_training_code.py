class SBFDataset(Dataset):
    def __init__(self, npz_path: str, crop_min: float = 0.70, crop_max: float = 0.90):
        print(f"Loading .npz cache: {npz_path}")
        cache = np.load(npz_path, allow_pickle=True)

        all_images  = cache["images"]
        all_log_distances = cache["log_distances"]
        all_paths = list(cache["paths"])

        all_dists_mpc = 10 ** all_log_distances
        valid_mask = all_dists_mpc > 40.0
        n_total  = len(all_paths)
        n_removed  = int((~valid_mask).sum())
        valid_idx  = np.where(valid_mask)[0]

        self.images   = all_images[valid_idx]
        self.log_distances = all_log_distances[valid_idx]
        self.paths  = [all_paths[i] for i in valid_idx]
        self.crop_min   = crop_min
        self.crop_max = crop_max
        self.groups  = np.array([galaxy_group(p) for p in self.paths])

        dists = 10 ** self.log_distances
        print(f"  Loaded {n_total} images; removed {n_removed} with d <= 40 Mpc; "
              f"kept {len(self.paths)}")
        print(f"  Distance range : {dists.min():.1f} - {dists.max():.1f} Mpc")
        print(f"  log10(d) range : {self.log_distances.min():.3f} - "
              f"{self.log_distances.max():.3f}")
        print(f"  Unique galaxies: {len(set(self.groups))}  |  "
              f"Total images: {len(self.paths)}")

    def __len__(self):
        return len(self.paths)

    def __getitem__(self, idx, augment: bool = False):
        image = self.images[idx].astype(np.float32)
        image = normalise(image)
        tensor = torch.from_numpy(image).unsqueeze(0).repeat(3, 1, 1)
        label = torch.tensor(self.log_distances[idx], dtype=torch.float32)
        return tensor, label

class AugSubset(Dataset):
    def __init__(self, subset: Subset, augment: bool):
        self.subset  = subset
        self.augment = augment

    def __len__(self):
        return len(self.subset)

    def __getitem__(self, idx):
        real_idx = self.subset.indices[idx]
        return self.subset.dataset.__getitem__(real_idx, augment=self.augment)

#allow batch size = 1
def collate_single(batch):
    tensor, label = batch[0]
    return tensor.unsqueeze(0), label.unsqueeze(0)

def make_loader(dataset, indices, args, device, training: bool):
    aug_subset = AugSubset(Subset(dataset, indices), augment=training)

    if training:
        dists_mpc = 10 ** dataset.log_distances[indices]
        bin_edges = [40, 60, 90, 120, np.inf]
        bin_idx  = np.digitize(dists_mpc, bin_edges) - 1
        bin_counts = np.bincount(bin_idx, minlength=len(bin_edges) - 1)
        bin_counts = np.maximum(bin_counts, 1)
        sample_weights = 1.0 / bin_counts[bin_idx]
        #random weighted sampler to prevent impact of class imbalance
        sampler = WeightedRandomSampler(
            weights = torch.DoubleTensor(sample_weights),
            num_samples = len(indices),
            replacement = True,
        )
        return DataLoader(
            aug_subset,
            batch_size  = 1,
            sampler = sampler,
            num_workers = args.num_workers,
            pin_memory = (device.type == "cuda"),
            collate_fn = collate_single,
        )
    else:
        return DataLoader(
            aug_subset,
            batch_size  = 1,
            shuffle = False,
            num_workers = args.num_workers,
            pin_memory = (device.type == "cuda"),
            collate_fn  = collate_single,
        )

#model
class SBFEfficientNetB0(nn.Module):
    def __init__(self, pretrained: bool = True, dropout: float = 0.3):
        super().__init__()
        weights  = EfficientNet_B0_Weights.IMAGENET1K_V1 if pretrained else None
        backbone = models.efficientnet_b0(weights=weights)
        in_features = backbone.classifier[1].in_features
        backbone.classifier = nn.Sequential( #here we change classification to regression head
            nn.Dropout(p=dropout),
            nn.Linear(in_features, 256),
            nn.ReLU(inplace=True),
            nn.Dropout(p=dropout / 2),
            nn.Linear(256, 1),
        )
        replace_bn_with_gn(backbone)
        self.backbone = backbone

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.backbone(x).squeeze(1)

#discriminative fine-tuning
def build_optimizer(model, base_lr, lr_early=0.01, lr_mid=0.1):
    f = model.backbone.features
    early_params = (list(f[0].parameters()) + list(f[1].parameters()) +
                    list(f[2].parameters()) + list(f[3].parameters()))
    mid_params = (list(f[4].parameters()) + list(f[5].parameters()) +
                    list(f[6].parameters()))
    head_params = (list(f[7].parameters()) + list(f[8].parameters()) +
                    list(model.backbone.classifier.parameters()))
    return torch.optim.Adam([
        {"params": early_params, "lr": base_lr * lr_early},
        {"params": mid_params, "lr": base_lr * lr_mid},
        {"params": head_params,  "lr": base_lr * 1.0},
    ])

#training and eval loops
def train_one_epoch(model, loader, optimizer, criterion, device):
    model.train()
    total_loss, total_count = 0.0, 0
    for images, labels in loader:
        images, labels = images.to(device), labels.to(device)
        optimizer.zero_grad()
        preds = model(images)
        loss = criterion(preds, labels)
        loss.backward()
        optimizer.step()
        total_loss  += loss.item() * len(labels)
        total_count += len(labels)
    return total_loss / total_count


@torch.no_grad()
def evaluate(model, loader, criterion, device):
    model.eval()
    total_loss, total_count = 0.0, 0
    all_preds, all_labels   = [], []
    for images, labels in loader:
        images, labels = images.to(device), labels.to(device)
        preds = model(images)
        total_loss  += criterion(preds, labels).item() * len(labels)
        total_count += len(labels)
        all_preds.append(preds.cpu())
        all_labels.append(labels.cpu())
    return total_loss / total_count, torch.cat(all_preds), torch.cat(all_labels)

